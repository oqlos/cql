/** Extract SET/OPT variables from all GOALs for display */
export function extractSetVarsFromGoals(defData: DefData): Array<{ goalName: string; goalIdx: number; varName: string; value: string; type: 'set' | 'opt'; lineIdx: number }> {
  const goals = defData.library.goals || [];
  const vars: Array<{ goalName: string; goalIdx: number; varName: string; value: string; type: 'set' | 'opt'; lineIdx: number }> = [];
  
  for (let goalIdx = 0; goalIdx < goals.length; goalIdx++) {
    const goal = goals[goalIdx];
    const code = goal.code || '';
    const lines = code.split('\n');
    
    for (let lineIdx = 0; lineIdx < lines.length; lineIdx++) {
      const parsed = parseGoalVariableLine(lines[lineIdx]);
      if (parsed) {
        vars.push({
          goalName: goal.name,
          goalIdx,
          varName: parsed.varName,
          value: parsed.value,
          type: parsed.type,
          lineIdx
        });
      }
    }
  }
  
  return vars;
}
